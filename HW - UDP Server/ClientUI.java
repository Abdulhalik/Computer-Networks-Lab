    private void connectBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_connectBtnActionPerformed
        // TODO add your handling code here:
        try {
            String host = ipField.getText();
            int port = Integer.parseInt(portField.getText());

            client = new UDP_Client();
            client.start(port, historyPane, jLabelName);

            disableConnectButton();
        } catch (IOException ex) {
            System.out.println("Error - connectBtnActionPerformed : " + ex);
            enableConnectButton();
        }
    }//GEN-LAST:event_connectBtnActionPerformed

    private void sendMessageBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendMessageBtnActionPerformed
        // TODO add your handling code here:
        try {
            String message = messagePane.getText();

            if (!message.isEmpty()) {
                // String mesaj g√∂nder
                client.sendMessage(message);
                messagePane.setText("");
            }

            if (message.equals("son")) {
                disconnectBtnActionPerformed(evt);
            }
        } catch (IOException ex) {
            System.out.println("Error - sendMessageBtnActionPerformed : " + ex);
            enableConnectButton();
        }
    }//GEN-LAST:event_sendMessageBtnActionPerformed

    private void disconnectBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_disconnectBtnActionPerformed
        // TODO add your handling code here:
        try {
            client.disconnect();
            enableConnectButton();
        } catch (IOException ex) {
            System.out.println("Error - disconnectBtnActionPerformed : " + ex);
        }
    }//GEN-LAST:event_disconnectBtnActionPerformed

